/*
7장 연산자
연산자는 하나 이상의 표현식을 대상으로 산술, 할당, 비교, 논리. 타입, 지수, 연산 등을 수행해 하나의 값을 만든다.
이때 연산 대상을 피연산자라 한다. 피연산자는 값으로 평가될 수 있는 표현식이어야 한다.
그리고 피연산자와 연산자의 조합으로 이뤄진 연산자 표현식도 값으로 평가될 수 있는 표현식이다.

산술 연산자: 5 * 4 -> 20
문자열 연결 연산자: 'My name is ' + 'websteak' -> 'My name is websteak'
할당 연산자: color = 'red' -> 'red'
비교 연산자: 3 > 5 -> false
논리 연산자: true && false -> false
타입 연산자: typeof 'hi' -> string

피연산자가 '값'이라는 명사의 역할을 한다면 연산자는 '피연산자를 연산하여 새로운 값을 만든다' 라는 동사의 역할을 한다고 볼 수 있다.
피연산자는 연산의 대상이 되어야 하므로 값으로 평가할 수 있어야 한다.
연산자는 값으로 평가된 피연산자를 연산해 새로운 값을 만든다.
*/